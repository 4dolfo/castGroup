package com.group.cast.bean;

public class Usuario {
	private String vcNome;
	private String vcEmail;
	private String vcToken;
	
	public String getVcEmail() {
		return vcEmail;
	}
	public void setVcEmail(String vcEmail) {
		this.vcEmail = vcEmail;
	}
	public String getVcToken() {
		return vcToken;
	}
	public void setVcToken(String vcToken) {
		this.vcToken = vcToken;
	}
	public String getVcNome() {
		return vcNome;
	}
	public void setVcNome(String vcNome) {
		this.vcNome = vcNome;
	}
}
