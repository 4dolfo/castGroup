CREATE TABLE USER (
  NU_ID_USER BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  VC_NAME VARCHAR(64),
  VC_EMAIL VARCHAR(64),
  VC_PASSWORD VARCHAR(500),
  NU_STATUS BIGINT NOT NULL,
  DT_REGISTER DATE
);

CREATE TABLE EQUIPE(
  NU_ID_EQUIPE BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  VC_NAME VARCHAR(250),
  NU_STATUS BIGINT NOT NULL,
  DT_REGISTER DATE
);



CREATE TABLE FUNCIONARIO(
  NU_ID_FUNCIONARIO BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  NU_ID_EQUIPE BIGINT NOT NULL,
  VC_NAME VARCHAR(250),
  DT_NASCIMIENTO DATE,
  DT_CONTRATO DATE,
  VC_URL_FOTO VARCHAR(500),
  NU_STATUS BIGINT NOT NULL,
  DT_REGISTER DATE
);

CREATE TABLE ADDRESS(
  NU_ID_ADDRESS BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  NU_ID_FUNCIONARIO BIGINT NOT NULL,  
  VC_RUA VARCHAR(250),
  VC_NUMERO VARCHAR(250),
  VC_COMPLEMENTO VARCHAR(250),
  VC_BAIRRO VARCHAR(250),
  VC_CIDADE VARCHAR(250),
  VC_ESTADO VARCHAR(250),
  NU_STATUS BIGINT NOT NULL,
  DT_REGISTER DATE
);

ALTER TABLE FUNCIONARIO
    ADD FOREIGN KEY (NU_ID_EQUIPE) 
    REFERENCES EQUIPE(NU_ID_EQUIPE);


CREATE TABLE FERIAS(
  NU_ID_FERIAS BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
  NU_ID_FUNCIONARIO BIGINT NOT NULL,  
  NU_ID_EQUIPE BIGINT NOT NULL,  
  DT_INICIO DATE,
  DT_FIN  DATE,
  NU_STATUS BIGINT NOT NULL,
  DT_REGISTER DATE
);



